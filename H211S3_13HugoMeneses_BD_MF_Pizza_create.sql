--vista para Cliente
CREATE VIEW vCliente
AS
SELECT
     C.IDCLI AS IDCLI, 
     C.NOMCLI AS NOMCLI,
     C.CORCLI AS CORCLI,
	 C.CELCLI AS CELCLI,
	 C.DNICLI AS DNICLI,
	 C.DIRCLI AS DIRCLI,
	 C.ESTCLI AS ESTCLI,
     U.CODUBI AS CODUBI,

	 ROW_NUMBER() OVER (ORDER BY C.IDCLI DESC) AS ORDEN
FROM 
     CLIENTE C
 INNER JOIN UBIGEO U ON
	 C.CODUBI = U.CODUBI
GO
--vista para Administrador
CREATE VIEW vAdministrador
AS
SELECT
     A.IDADM AS IDADM, 
     A.NOMADM AS NOMCLI,
     A.CORADM AS CORADM,
	 A.CELADM AS CELADM,
	 A.DNIADM AS DNIADM,
	 A.DIRADM AS DIRADM,
	 A.ESTADM AS ESTADM,
     U.CODUBI AS CODUBI,

	 ROW_NUMBER() OVER (ORDER BY A.IDADM DESC) AS ORDEN
FROM 
     ADMINISTRADOR A
 INNER JOIN UBIGEO U ON
	 A.CODUBI = U.CODUBI
GO

